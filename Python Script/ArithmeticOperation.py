# Arithmetic Operation
num1 = 100
num2 = 9.9

#Addition between two numbers
sum = num1 + num2
print("Addition {0} + {1} = {2}".format(num1, num2, sum))

#Subtraction between two numbers
sub = num1 - num2 
print("Subtraction {0} - {1} = {2}".format(num1, num2, sub))

#Multiplication between two numbers
mul = num1 * num2
print("Multplication {0} x {1} = {2} ".format(num1, num2, mul))

#Division between two numbers
div = num1 / num2
print("Division {0} / {1} = {2}".format(num1, num2, div))

#Module between two numbers
mod = num1 % num2
print("Modularisation {0} % {1} = {2}".format(num1, num2, mod))
